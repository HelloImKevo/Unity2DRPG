public enum ElementType
{
    /// <summary>
    /// Neutral damage (no affiliated elemental properties).
    /// </summary>
    None,

    /// <summary>
    /// Inflicts fire-based damage and burning effects.
    /// </summary>
    Fire,

    /// <summary>
    /// Inflicts ice-based cold damage and slowing / freezing effects.
    /// </summary>
    Ice,

    /// <summary>
    /// Inflicts lightning-based electric damage and shock effects.
    /// </summary>
    Lightning
}
